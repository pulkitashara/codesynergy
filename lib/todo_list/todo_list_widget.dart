}

class _TodoListWidgetState extends State<TodoListWidget> {
  late TodoListModel _model;

  final scaffoldKey = GlobalKey<ScaffoldState>();

  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => TodoListModel());
